export function getPhotosByRarity(photos: CharacterPhoto[], rarity: PhotoRarity): CharacterPhoto[] {
  return photos.filter(photo => photo.rarity === rarity);
}

export function getUnlockedPhotos(photos: CharacterPhoto[]): CharacterPhoto[] {
  return photos.filter(photo => photo.unlocked);
}

export function getNextPhotoToUnlock(photos: CharacterPhoto[], currentAffection: number): CharacterPhoto | null {
  const lockedPhotos = photos.filter(photo => !photo.unlocked && currentAffection < photo.unlockedAt);
  return lockedPhotos.sort((a, b) => a.unlockedAt - b.unlockedAt)[0] || null;
}

export function checkPhotoUnlocks(photos: CharacterPhoto[], currentAffection: number): CharacterPhoto[] {
  return photos.map(photo => {
    if (!photo.unlocked && currentAffection >= photo.unlockedAt) {
      return {
        ...photo,
        unlocked: true,
        unlockedDate: new Date()
      };
    }
    return photo;
  });
}